enum Srpites{
	SPRITE_0 = 0,
	SPRITE_9 = SPRITE_0 + 9,
	SPRITE_a,
	SPRITE_z = SPRITE_a + 25,

	SPRITE_COMMA,
	SPRITE_DOT,
	SPRITE_EXCLAMATION,
	SPRITE_INTERROGATION,
	SPRITE_OPEN_QUOTES,
	SPRITE_CLOSE_QUOTES,
	SPRITE_COLUMN,
	SPRITE_SEMI_COLUMN,
	SPRITE_OPENPAREN,
	SPRITE_CLOSEPAREN,
	SPRITE_PERCENT,
	SPRITE_SPACE,
	SPRITE_DASH,
	SPRITE_UNDERLINE,
	SPRITE_EQUAL,
	SPRITE_PLUS,
	SPRITE_BACKSLASH,
	SPRITE_SLASH,

    // for counting purposes
    SPRITE_COUNT
};

static inline int get_sprite_from_char(char c){
	switch (c)
	{
	case ',':	return SPRITE_COMMA;
	case '.':	return SPRITE_DOT;
	case '!':	return SPRITE_EXCLAMATION;
	case '?':	return SPRITE_INTERROGATION;
	case '\"':	return SPRITE_OPEN_QUOTES;
	case ':':	return SPRITE_COLUMN;
	case ';':	return SPRITE_SEMI_COLUMN;
	case '(':
	case '[':	return SPRITE_OPENPAREN;
	case ')':
	case ']':	return SPRITE_CLOSEPAREN;
	case '%':	return SPRITE_PERCENT;
	case ' ':	return SPRITE_SPACE;
	case '-':	return SPRITE_DASH;
	case '_': 	return SPRITE_UNDERLINE;
	case '=':	return SPRITE_EQUAL;
	case '+': 	return SPRITE_PLUS;
	case '\\':	return SPRITE_BACKSLASH;
	case '/':	return SPRITE_SLASH;
	default:
		if(c >= '0' && c <= '9') return (int) (SPRITE_0 + c - '0');
		if(c >= 'a' && c <= 'z') return (int) (SPRITE_a + c - 'a');
		if(c >= 'A' && c <= 'Z') return (int) (SPRITE_a + c - 'A');
		return -1;
	}
}

